    public void jmlSatuGrup(){
        String ambilJml = txfJmlAnggotaTim.getText();
        if(txfJmlAnggotaTim.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Masukan jumlah orang dalam grup!");
        }else if (Integer.parseInt(ambilJml) > 0) {
            lbJmlAnggotaTim.setText(ambilJml);
            txfJmlAnggotaTim.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Input harus berupa angka lebih dari 0!");
        }
    }    

    public void pembagianGrup(){
        ArrayList<String> dataBagiBaru = new ArrayList<>();
        
        int no = 1;
        String ambilJml = lbJmlAnggotaTim.getText();
        lbJmlAnggotaTim.setText(ambilJml);
        
        txaHasilAcak.append(no + ").  ");
        for (int i = 0; i < dataNamaTeracak.size(); i++) {
            dataBagiBaru.add(dataNamaTeracak.get(i));
            txaHasilAcak.append(dataNamaTeracak.get(i));
            
            if(dataBagiBaru.size() > Integer.valueOf(ambilJml)-1){
                txaHasilAcak.append("\n");
                dataBagiBaru.removeAll(dataBagiBaru);

                if (i + 1 < dataNamaTeracak.size()) {
                        txaHasilAcak.append(no + 1 + ").  ");
                        no++;
                    }
                continue;    
            } 
            if (i + 1 != dataNamaTeracak.size()) {
                txaHasilAcak.append(" ~ ");
            }
        }
    }
