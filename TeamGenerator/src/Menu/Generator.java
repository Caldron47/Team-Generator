    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (dataNamaTeracak.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hasil pengacakan kosong, tidak dapat menyimpan!");
        }else if (txfNama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap mengisi judul pengacakan terlebih dahulu");
        }else {
            try{
                String dataNamaTeracakString = String.join(",", dataNamaTeracak);
                String sql = "SELECT MAX(id_hasil) FROM hasil_acak";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                res.next();
                int saveID = res.getInt(1)+1;

                sql = "SELECT MAX(urutan_hasil) FROM hasil_acak WHERE username = '" + user + "'";
                java.sql.Statement stmUrutanData = conn.createStatement();
                java.sql.ResultSet resUrutanData = stmUrutanData.executeQuery(sql);
                resUrutanData.next();
                int urutanSave = resUrutanData.getInt(1)+1;

                sql = "INSERT INTO hasil_acak VAlUES ('" + saveID + "','" + txfNama.getText() + "','" + dataNamaTeracakString + "','" + jmlAnggotaSaatAcak + "','" + urutanSave + "','" + user + "')";
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil!");
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

