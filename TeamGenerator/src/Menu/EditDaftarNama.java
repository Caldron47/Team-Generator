    private void btnNama2ActionPerformed(java.awt.event.ActionEvent evt) {
        if (dataNama.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data nama kosong, tidak dapat menyimpan");
        }else if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap mengisi judul daftar nama terlebih dahulu");
        }else {
            try{
                String dataNamaString = String.join(",", dataNama);
                String sql = "SELECT MAX(id_daftar) FROM daftar_nama";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);
                res.next();
                int saveID = res.getInt(1)+1;

                sql = "SELECT MAX(urutan_daftar) FROM daftar_nama WHERE username = '" + user + "'";
                java.sql.Statement stmUrutanData = conn.createStatement();
                java.sql.ResultSet resUrutanData = stmUrutanData.executeQuery(sql);
                resUrutanData.next();
                int urutanSave = resUrutanData.getInt(1)+1;
                   
                sql = "INSERT INTO daftar_nama VAlUES ('" + saveID + "','" + jTextField2.getText() + "','" + dataNamaString + "','" + urutanSave + "','" + user + "')";
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil!");
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }
    
