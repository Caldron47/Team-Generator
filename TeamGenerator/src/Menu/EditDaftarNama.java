    public void tampilkanDariDB() {
        txaDaftarNama.setText("");
        dataNama.stream().forEach((a) -> {
            nomorUrut++;
            txaDaftarNama.append(nomorUrut + ". " + a + "\n");
        });
            
        txfNama.setText("");
        lbJmlNama.setText((dataNama.size()) + "");
    }
    
    public void addNama(){
        
        String nama = txfNama.getText();
        if("".equals(nama)){
            JOptionPane.showMessageDialog(this, "Masukan nama !");
        }else{
            dataNama.add(nama);
            /*
            if (nomorUrut == 3) {
                dataNama.remove(1);
            }*/
            nomorUrut = 0;
            txaDaftarNama.setText("");
            dataNama.stream().forEach((a) -> {
                nomorUrut++;
                txaDaftarNama.append(nomorUrut + ". " + a + "\n");
            });
            
            txfNama.setText("");
            lbJmlNama.setText((dataNama.size()) + "");
        }
    }
   
    public void hapusSatuNama(){
        if (Integer.parseInt(txfHapusSatu.getText()) >= 1 && Integer.parseInt(txfHapusSatu.getText()) <= dataNama.size()) {
            txaDaftarNama.setText("");
            dataNama.remove(Integer.parseInt(txfHapusSatu.getText()) - 1);
            nomorUrut = 0;
            dataNama.stream().forEach((a) -> {
                    nomorUrut++;
                    txaDaftarNama.append(nomorUrut + ". " + a + "\n");
            });
        
        lbJmlNama.setText(Integer.toString(dataNama.size()));
        } else {
            JOptionPane.showMessageDialog(this, "Harap masukkan nomor yang ada di daftar nama");
        }
    } 
                    
